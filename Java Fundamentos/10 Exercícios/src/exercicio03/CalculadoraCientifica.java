package exercicio03;

public class CalculadoraCientifica {

	// Seno
	protected double seno(double numero1) {
		return Math.sin(numero1);
	}
	
	// Cosseno
	protected double cosseno(double numero) {
		return Math.cos(numero);
	}
	
	// Tangente
	protected double tangente(double numero) {
		return Math.tan(numero);
	}
	
}
